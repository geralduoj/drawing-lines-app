package com.example.drawlines;

public class Coordinates {

    public int startX;
    public int startY;
    public int endX;
    public int endY;



    public Coordinates(int sx, int sy, int ex, int ey)
    {
        this.startX = sx;
        this.startY = sy;
        this.endX = ex;
        this.endY = ey;
    }
}
